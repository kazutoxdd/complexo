-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
vSERVER = Tunnel.getInterface("service")
-----------------------------------------------------------------------------------------------------------------------------------------
-- SERVICELIST
-----------------------------------------------------------------------------------------------------------------------------------------
local serviceList = {
	{ -914.4,-2026.97,10.65,"Police-1",1.5,6 }, --dpaeroporto
	{ 377.27,-1605.78,31.17,"Police",1.5,6 }, --dip
	--{ 1833.75,3678.34,34.27,"Sheriff-1",1.0,47 },--cu
	--{ -447.28,6013.01,32.41,"Sheriff-2",1.0,47 }, --cu
	{ 1840.20,2578.48,46.07,"Corrections",1.0,60 },--Prisao
